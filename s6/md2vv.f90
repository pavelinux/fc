MODULE VARIABLES
IMPLICIT NONE
INTEGER :: N,NN,NPASOS
REAL*8 :: RHO,LX,LY,DX,DY,RIJ,UPOT,UKIN,UTOT,ULJ,UCUT,M_X,M_Y,DELTA_T,R_CUT
REAL*8 :: SGM = 1.0, EPS=1.0
REAL*8, DIMENSION(:), ALLOCATABLE::RX,RY
REAL*8, DIMENSION(:), ALLOCATABLE::VX,VY
REAL*8, DIMENSION(:), ALLOCATABLE::FX,FY
REAL*8  RND
REAL*8 :: T_I,T_F
END MODULE VARIABLES

MODULE UNIDADES_REDUCIDAS
IMPLICIT NONE
! parametros lj para el Ar
REAL*8 :: S_AR = 3.4E-10, EPS_AR=1.65E-21,M_AR=6.69E-26
REAL*8 :: T_R, L_R, RHO_R, UTOT_R
! K-Boltzman
REAL*8 :: kB = 1.3806E-23
END MODULE UNIDADES_REDUCIDAS

PROGRAM MD_F
USE VARIABLES
IMPLICIT NONE
CALL CPU_TIME(T_I)
CALL COORDENADAS
CALL CELDA
CALL FUERZAS
CALL MDLOOP
CALL CPU_TIME(T_F)
CALL LOGFILE
END PROGRAM MD_F

SUBROUTINE COORDENADAS
USE VARIABLES
IMPLICIT NONE
INTEGER :: I,J,K,M

OPEN(1,FILE='input.txt',STATUS='OLD',ACTION='READ')
READ(1,*)
READ(1,*)
READ(1,*)NPASOS
READ(1,*)N
READ(1,*)RHO
READ(1,*)DELTA_T
READ(1,*)R_CUT
READ(1,*)
CLOSE(1)

ALLOCATE(RX(N),RY(N))
ALLOCATE(VX(N),VY(N))
ALLOCATE(FX(N),FY(N))

LX = SQRT(DBLE(N/RHO))
LY = LX
NN = SQRT(DBLE(N)) + 1 
DX = LX/DBLE(NN)
DY = DX
K=0
DO I=1,NN 
     DO J=1,NN
         IF(K<N) THEN
             K=K+1
             RX(K) = DBLE(I) * DX
             RY(K) = DBLE(J) * DY
         END IF
     END DO
END DO
M_X = 0.0
M_Y = 0.0
DO I =1, N
    CALL RANDOM_NUMBER(RND)
    VX(I) = 2.0 * RND - 1.0
    VY(I) = 2.0 * RND - 1.0
    M_X = M_X + VX(I)
    M_Y = M_Y + VY(I)
END DO
M_X = M_X / DBLE(N)
M_Y = M_Y / DBLE(N)
DO I = 1, N
    VX(I) = VX(I) - M_X
    VY(I) = VY(I) - M_Y
END DO
END SUBROUTINE COORDENADAS

SUBROUTINE CELDA
USE VARIABLES
IMPLICIT NONE
INTEGER :: I
OPEN(2,FILE='coordenadas.xyz', STATUS='UNKNOWN', ACTION='WRITE')
WRITE(2,*) N
WRITE(2,*)
DO I=1,N
    WRITE(2,*)'C ', RX(I), RY(I), 0.0
END DO
CLOSE(2)
END SUBROUTINE CELDA

