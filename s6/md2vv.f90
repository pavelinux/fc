SUBROUTINE FUERZAS
USE VARIABLES
IMPLICIT NONE
INTEGER :: I, J
REAL*8 :: DULJ
DO I=1, N
    FX(I) = 0.0
    FY(I) = 0.0
END DO
UPOT = 0.0
DO I = 1, NN - 1
 DO J = I + 1, NN 
     DX = RX(I) - RX(J)
     DY = RY(I) - RY(J)
    ! MINIMA IMAGEN
    IF(DX > 0.5 * LX) THEN
        DX = DX - LX
    ELSEIF(DX < - 0.5 * LX) THEN
        DX = DX + LX
    ENDIF 
    IF(DY > 0.5 * LY) THEN
        DY = DY - LY
    ELSEIF(DY < - 0.5 * LY) THEN
        DY = DY + LY
    ENDIF 
    !
     RIJ = SQRT(DX * DX + DY * DY)
      IF (RIJ<= R_CUT) THEN
!           ULJ = 4.0 * EPS * ((SGM/RIJ)**6 * ( (SGM/RIJ)**6 - 1.0)) - UCUT
           ULJ = 4.0 * EPS * ((SGM/RIJ)**6 * ( (SGM/RIJ)**6 - 1.0)) 
           UPOT = UPOT + ULJ
           DULJ = 48.0 * EPS * (SGM/RIJ)**6 * ((SGM/RIJ)**6 - 0.5)/(RIJ * RIJ)
           FX(I) = FX(I) + DULJ * DX
           FY(I) = FY(I) + DULJ * DY
           FX(J) = FX(J) - DULJ * DX
           FY(J) = FY(J) - DULJ * DY
        END IF
 END DO 
END DO
!WRITE(*,'(2F12.7)') SUM(FX),SUM(FY)
!WRITE(*,*) SUM(FX),SUM(FY)
!STOP
END SUBROUTINE FUERZAS
